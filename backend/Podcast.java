class Podcast {
    // --- ATTRIBUTI ---
    String nome;
    String descrizione;
    String[] tags;
    double[] pesi;
    String link;
    String immagine;
    
    // --- COSTRUTTORE ---
    Podcast (String nome, String descrizione, String[] tags, double[] pesi, String link, String immagine){
        this.nome = nome;
        this.descrizione = descrizione;
        this.tags = tags;
        this.pesi = pesi;
        this.link = link;
        this.immagine = immagine;
    }

    // --- GETTER E SETTER ---
    
}